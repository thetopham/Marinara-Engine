async function readManifestFolders(baseDir, manifestFile = "manifest.ts") {
  const entries = await readdir(baseDir, { withFileTypes: true });
  const manifests = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const manifestPath = join(baseDir, entry.name, manifestFile);
    let text = "";
    try {
      text = await readFile(manifestPath, "utf8");
    } catch {
      continue;
    }
    const match = text.match(/export\s+const\s+([A-Za-z0-9_]+)\s*=/);
    if (!match?.[1]) {
      throw new Error(`Could not find exported manifest const in ${manifestPath}`);
    }
    manifests.push({ folder: entry.name, exportName: match[1] });
  }

  return manifests;
}

function sortByPreferredOrder(manifests, preferredOrder) {
  const order = new Map(preferredOrder.map((folder, index) => [folder, index]));
  return manifests.sort((a, b) => {
    const aOrder = order.get(a.folder);
    const bOrder = order.get(b.folder);
    if (aOrder !== undefined && bOrder !== undefined) return aOrder - bOrder;
    if (aOrder !== undefined) return -1;
    if (bOrder !== undefined) return 1;
    return a.folder.localeCompare(b.folder);
  });
}

async function writeRegistry({
  baseDir,
  outputPath,
  typeImport,
  arrayName,
  elementType,
  importPrefix = ".",
  manifestFile = "manifest.ts",
  preferredOrder,
}) {
  const manifests = sortByPreferredOrder(await readManifestFolders(baseDir, manifestFile), preferredOrder);
  const manifestModule = manifestFile.replace(/\.ts$/, ".js");
  const lines = [
    "// This file is generated by packages/shared/scripts/generate-feature-registries.mjs.",
    `// Add a new folder with ${manifestFile}, then run pnpm --filter @marinara-engine/shared build.`,
    typeImport,
    "",
    ...manifests.map(
      ({ folder, exportName }) => `import { ${exportName} } from "${importPrefix}/${folder}/${manifestModule}";`,
    ),
    "",
    `export const ${arrayName}: readonly ${elementType}[] = [`,
    ...manifests.map(({ exportName }) => `  ${exportName},`),
    "];",
    "",
  ];

  await writeFile(outputPath, `${lines.join("\n")}\n`, "utf8");
}

